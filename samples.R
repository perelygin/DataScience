 c<-1:n
 for(i in 0:(n-1)){
   x[i+1,1:(n-i)]<-c[1:(n-i)]  
   if(n-i+1 <= n){
     x[i+1,(n-i+1):n]<- n-i
   }
 }
 if(n<=1){
   #print(x)
   return(x)
 }else{
   #print(x)
